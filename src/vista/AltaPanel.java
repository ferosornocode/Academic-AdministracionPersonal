    private void enviarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarButtonActionPerformed
        String nombre = nombreText.getText();
        String apPaterno = apPaternoText.getText();
        String apMaterno = apMaternoText.getText();
        int sexo=-1;
        if(masculinoRadio.isSelected())
            sexo = 0;
        if(femeninoRadio.isSelected())
            sexo = 1;
        String correo = correoText.getText();
        String telefono = telefonoText.getText();
        String edadString = (String) edadComboBox.getSelectedItem();
        Integer edadInteger = new Integer(edadString);
        int edad = edadInteger.intValue();
        String sueldoString = sueldoText.getText();
        Float sueldoFloat = new Float(sueldoString);
        float sueldo = sueldoFloat.floatValue();
        //String nombre, String apPaterno, 
        //String apMaterno, String telefono, String correo, int sexo, int edad, float sueldo
        Empleado empleado = new Empleado(nombre,apPaterno,apMaterno,telefono,correo,sexo,edad,sueldo);
        ControladorBaseDeDatos  cont = new ControladorBaseDeDatos(); 
        
        if(cont.guardarEmpleadoControlador(empleado))
        {
            JOptionPane.showMessageDialog(this, "Guardado exitosamente.", "Guardado", JOptionPane.PLAIN_MESSAGE);
            limpiarCampos();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Error, verifique sus datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_enviarButtonActionPerformed
